package com.mycompany.javasalessystem.Models;

import com.mycompany.javasalessystem.Utils.Encrypt;

public abstract class User {
    private String id;
    private String name;
    private String email;
    private String password;
    private String cpf;

    public User(String id, String name, String email, String password, String cpf) {
        String encryptPassword = Encrypt.hashMD5(password);
        
        this.id = id;
        this.name = name;
        this.email = email;
        this.password = encryptPassword;
        this.cpf = cpf;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    
    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }
    
}
